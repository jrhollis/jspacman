    get isAlternateVersion() {
        return !(this.level <= 13 || (this.level > 14 && !((this.level - 1) % 4)));
    }

    get pelletColor() {
        if (!this.isAlternateVersion) { 
            return '#fc0d1b';
        } else {
            return '#2dfffe';
        }
    }

    get textureOffset() {
        //depends on level
        if (!this.isAlternateVersion) { 
            // level 17, 19, 23, etc
            return {x: 228, y: 248*2};
        } else { 
            // alt color level 14, 15, 19, 23
            return {x: 228, y: 248*4};
        }
    }
