    get isAlternateVersion() {
        return !(this.level <= 13 || (this.level > 13 && (this.level % 4)));
    }
    
    get pelletColor() {
        if (!this.isAlternateVersion) { // level 15, 19
            return '#d8d9f7';
        } else {
            return '#dedffe';
        }
    }

    get textureOffset() {
        //depends on level
        if (!this.isAlternateVersion) { // level 15, 19
            return {x: 228, y: 248*3};
        } else {
            //alt color -- 16, 20
            return {x: 228, y: 248*5};
        }
    }
